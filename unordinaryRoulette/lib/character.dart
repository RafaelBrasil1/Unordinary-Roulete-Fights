class Character {
  final String name;
  final String gender;
  final String power;
  final String hairColor;
  final String eyeColor;
  final String origin;
  final double originModifier;

  Character({
    required this.name,
    required this.gender,
    required this.power,
    required this.hairColor,
    required this.eyeColor,
    required this.origin,
    required this.originModifier,
  });
}